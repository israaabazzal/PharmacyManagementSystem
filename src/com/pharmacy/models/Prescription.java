package com.pharmacy.models;

import java.util.Date;
import java.util.List;

public class Prescription {
    private int prescriptionId;
    private int patientId;
    private int doctorId;
    private Date prescriptionDate;
    private String notes;
    private String status; // draft, active, completed, cancelled
    private int createdBy;
    private Integer approvedBy; // nullable
    private List<PrescriptionItem> items; // List of medicines prescribed

    // Getters and setters

    public int getPrescriptionId() {
        return prescriptionId;
    }

    public void setPrescriptionId(int prescriptionId) {
        this.prescriptionId = prescriptionId;
    }

    public int getPatientId() {
        return patientId;
    }

    public void setPatientId(int patientId) {
        this.patientId = patientId;
    }

    public int getDoctorId() {
        return doctorId;
    }

    public void setDoctorId(int doctorId) {
        this.doctorId = doctorId;
    }

    public Date getPrescriptionDate() {
        return prescriptionDate;
    }

    public void setPrescriptionDate(Date prescriptionDate) {
        this.prescriptionDate = prescriptionDate;
    }

    public String getNotes() {
        return notes;
    }

    public void setNotes(String notes) {
        this.notes = notes;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(int createdBy) {
        this.createdBy = createdBy;
    }

    public Integer getApprovedBy() {
        return approvedBy;
    }

    public void setApprovedBy(Integer approvedBy) {
        this.approvedBy = approvedBy;
    }

    public List<PrescriptionItem> getItems() {
        return items;
    }

    public void setItems(List<PrescriptionItem> items) {
        this.items = items;
    }
    @Override
public String toString() {
    return "Prescription #" + prescriptionId + " - " + new java.text.SimpleDateFormat("dd/MM/yyyy").format(prescriptionDate);
}

}

